package Ejercicio_Adi_06;
//El objetivo es que muestre los números del 100 al 0 utilizando la instrucción for.
public class Ejercicio_Adi_06 {
	public static void main (String args[]) {
		System.out.println("Numeros del 100 al 0:");
		for (int i=100;i>=0;i--) {
			System.out.println(i);
		}
	}
}