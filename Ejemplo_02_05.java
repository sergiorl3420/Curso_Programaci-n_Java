public class Ejemplo_02_05 {
    public static void main(String[] args) {
        //Pequeño ejercicio para comprobar como funciona el operador ++ y -- cuando se coloca antes o después de la
        //variable, viendo como la saca y como modifica en este caso el valor de la variable n. Un ejercicio sencillo.
        int n=8;
        System.out.print(n);
        System.out.print("/");
        System.out.print(n++);
        System.out.print("/");
        System.out.print(n);
    }
}
