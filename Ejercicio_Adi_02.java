package Ejercicio_Adi_02;
//El objetivo es que muestre los números del 1 al 100 utilizando la instrucción do while.
public class Ejercicio_Adi_02 {
	public static void main (String args[]) {
		int i=0;
		System.out.println("Numeros del 0 al 100: ");
		do {
			System.out.println(i);
			i++;
		}while (i<=100);
	}
}
