package Ejercicio_Adi_01;
//El objetivo es que muestre los números del 1 al 100 utilizando la instrucción while.
public class Ejercicio_Adi_01 {
	public static void main (String args[]) {
		int i=1;
		System.out.println("Numeros del 0 al 100: ");
		while (i<=100) {
			System.out.print(i);
			i++;
		}
	}
}
