package Ejercicio_Adi_04;
//El objetivo es que muestre los números del 100 al 0 utilizando la instrucción while.
public class Ejercicio_Adi_04 {
	public static void main (String args[]) {
		int i=100;
		System.out.println("Numeros del 100 al 0:");
		while (i>=0) {
			System.out.println(i);
			i--;
		}
	}
}