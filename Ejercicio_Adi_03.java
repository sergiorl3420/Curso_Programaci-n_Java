package Ejercicio_Adi_03;
//El objetivo es que muestre los números del 1 al 100 utilizando la instrucción for.
public class Ejercicio_Adi_03 {
	public static void main (String args[]) {
		System.out.println("Numeros del 0 al 100:");
		for (int i=0; i<=100; i++) {
			System.out.println(i);
		}
	}
}